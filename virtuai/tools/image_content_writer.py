def _headers():
    return {"Authorization": f"Bearer {KIE_API_KEY}", "Content-Type": "application/json"}


def _call_claude(system_msg: str, user_msg: str, max_tokens: int = 6000) -> str:
    resp = httpx.post(
        f"{KIE_CLAUDE_BASE}/messages",
        headers=_headers(),
        json={
            "model": MODEL,
            "max_tokens": max_tokens,
            "temperature": 0.85,
            "system": system_msg,
            "messages": [{"role": "user", "content": user_msg}],
        },
        timeout=120,
    )
    resp.raise_for_status()
    body = resp.json()
    content = body.get("content", [])
    text_parts = [c.get("text", "") for c in content if c.get("type") == "text"]
    return "\n".join(text_parts).strip()


def _strip_code_fence(s: str) -> str:
    s = s.strip()
    s = re.sub(r"^```(?:json)?\s*", "", s)
    s = re.sub(r"\s*```$", "", s)
    return s.strip()


# ── Portrait ────────────────────────────────────────────────────────────────

def _bullets(items: Optional[list[str]], limit: int = 8) -> str:
    if not items:
        return ""
    seen: list[str] = []
    for x in items[-limit:][::-1]:        # newest first, dedup
        if x and x not in seen:
            seen.append(x)
    return "\n".join(f"  ✗ {x}" for x in seen)


def _build_avoid_block(
    recent_topics:  Optional[list[str]],
    recent_outfits: Optional[list[str]],
    recent_moods:   Optional[list[str]],
    recent_scenes:  Optional[list[str]],
    recent_hooks:   Optional[list[str]],
) -> str:
    """Compile a 'do-not-repeat' brief for Claude from history dimensions."""
    sections: list[str] = []
    pairs = [
        ("topic patterns",          recent_topics),
        ("outfit descriptions",     recent_outfits),
        ("narrative moods",         recent_moods),
        ("scene locations / props", recent_scenes),
        ("opening hook patterns",   recent_hooks),
    ]
    for label, items in pairs:
        bulls = _bullets(items)
        if bulls:
            sections.append(f"\nRecently-used {label} — do NOT repeat:\n{bulls}")
    return "".join(sections)


PORTRAIT_SCHEMA = """
JSON schema for a single portrait post:
{
  "type": "portrait_quote",
  "topic": "string — 4-8 word title",
  "hook_summary": "string — the spoken hook line, ≤14 words",
  "headline": "string — the LARGE overlay text on the image, 2-6 words, all caps OK",
  "subhead": "string — smaller line beneath headline, 6-14 words",
  "image_prompt": "string — photo-real Nano Banana edit prompt placing the same MAN (Daniel, a man in his early 30s) in a specific real environment matching the message. Mention the outfit. Documentary candid style. No text in the image.",
  "post_caption_long": "string — the long Instagram/LinkedIn caption (200-500 words). Story-driven. Includes the headline as the opener. Ends with a quotable aphorism.",
  "hashtags": ["array", "of", "5-8", "lowercase", "hashtags"]
}
"""


def write_portrait(
    topic: Optional[str] = None,
    *,
    outfit: str = "dark polo shirt",
    recent_topics: Optional[list[str]] = None,
    mood: Optional[str] = None,
    recent_outfits: Optional[list[str]] = None,
    recent_moods: Optional[list[str]] = None,
    recent_scenes: Optional[list[str]] = None,
    recent_hooks: Optional[list[str]] = None,
) -> dict:
    """Generate a single portrait post: image prompt + headline + caption."""
    logger.info("Writing portrait post via Claude Sonnet 4.6...")
    avoid = _build_avoid_block(recent_topics, recent_outfits, recent_moods,
                                 recent_scenes, recent_hooks)
    topic_clause = f"Topic: {topic}\n" if topic else "Pick the topic yourself from the locked niche.\n"
    mood_clause = f"Mood: {mood}\n" if mood else ""

    user_msg = (
        f"Write ONE portrait quote post.\n\n"
        f"{topic_clause}{mood_clause}"
        f"Speaker outfit (must appear in image_prompt): {outfit}\n\n"
        + PORTRAIT_SCHEMA
        + avoid + "\n\nOutput ONLY the JSON object."
    )
    raw = _strip_code_fence(_call_claude(SYSTEM_PROMPT_BASE, user_msg))
    data = json.loads(raw)
    logger.info(f"  Portrait: '{data['headline']}' / '{data['subhead']}'")
    return data


