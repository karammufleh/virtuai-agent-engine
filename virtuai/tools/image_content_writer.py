# ── Caption shortcut ────────────────────────────────────────────────────────

def write_image_caption(content: dict) -> dict:
    """Extract platform-specific captions from already-written content.

    Instagram and LinkedIn historically used the same 2000+ char essay-style
    long caption. LinkedIn tolerates that. Instagram does not — IG captions
    read better when they're tight: hook + 1-2 short paragraphs + closing
    aphorism + hashtags. So we build the IG variant from the first 1-2
    paragraphs of the long caption + the slide-5 / aphorism payoff line.

    LinkedIn keeps the long-form variant unchanged.
    """
    long_cap = content.get("post_caption_long", "") or ""
    hashtags = " ".join(f"#{h.lstrip('#')}" for h in content.get("hashtags", []))
    return {
        "instagram": _build_ig_caption(content, long_cap, hashtags),
        "linkedin":  (long_cap + "\n\n" + hashtags)[:3000],
        "tweet":     (content.get("hook_summary", "") + "\n\n" + hashtags)[:280],
        "alt_text":  content.get("hook_summary", "")[:180],
    }


def _build_ig_caption(content: dict, long_cap: str, hashtags: str,
                       target_body_chars: int = 380) -> str:
    """
    Build a punchy Instagram-style caption from already-written content.

    Shape:
        <opening paragraph(s) up to ~target_body_chars>

        <aphorism / slide-5 headline, if available>

        <hashtags>

    The total stays well under IG's 2200-char limit. We split the long
    caption on blank lines, take whole paragraphs only (no mid-sentence
    cuts), and stop once we'd exceed the soft body budget.
    """
    # 1. Pick the body — whole paragraphs of long_cap, in order, up to budget.
    paragraphs = [p.strip() for p in long_cap.split("\n\n") if p.strip()]
    # Skip horizontal-rule paragraphs ("---" alone)
    paragraphs = [p for p in paragraphs if p.strip("- ") != ""]

    body_parts: list[str] = []
    body_len = 0
    for p in paragraphs:
        if body_len and body_len + len(p) > target_body_chars:
            break
        body_parts.append(p)
        body_len += len(p) + 2  # account for the blank line separator
    body = "\n\n".join(body_parts).strip()

    # 2. Optional closing — slide-5 headline (the quotable payoff) if it
    #    isn't already inside the body we picked.
    closing = ""
    slides = content.get("slides") or []
    if slides and isinstance(slides, list) and isinstance(slides[-1], dict):
        s5 = (slides[-1].get("headline") or "").strip().strip('"').strip()
        if s5 and s5.lower() not in body.lower():
            closing = f'\n\n"{s5}"'

    # 3. Hashtags
    tag_block = f"\n\n{hashtags}" if hashtags else ""

    caption = (body + closing + tag_block).strip()
    return caption[:2200]   # IG hard cap, safety net


